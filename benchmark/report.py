"""
Multi-model comparison report generation module for LLM Traffic Signal Cycle Benchmark.

Provides functions for generating comparison reports across multiple benchmark runs
and printing formatted terminal summaries.
"""

from __future__ import annotations

import csv
from datetime import datetime
from pathlib import Path
from typing import Any

from loguru import logger


# CSV columns for comparison report (from write_summary_csv_extended)
COMPARISON_COLUMNS = [
    "model_name",
    "scenario",
    "format_success_rate",
    "constraint_satisfaction_rate",
    "phase_order_correct_rate",
    "response_time_avg",
    "queue_vehicles_avg",
    "total_delay_avg",
    "throughput",
]


def generate_comparison_report(results_dir: Path) -> Path:
    """Generate a multi-model comparison report from benchmark results.

    Scans the results directory for subdirectories containing benchmark outputs,
    reads the single-row CSV files generated by write_summary_csv_extended,
    and merges them into a single comparison CSV.

    Args:
        results_dir: Path to the benchmark results directory containing
                     model subdirectories (e.g., qwen3-4b_2026-02-15_18-30-00/)

    Returns:
        Path to the generated comparison CSV file

    The output file is named comparison_{timestamp}.csv and is placed in
    the results_dir root.
    """
    results_path = Path(results_dir)

    if not results_path.exists():
        raise FileNotFoundError(f"Results directory not found: {results_dir}")

    # Collect all model CSV files
    model_rows: list[dict[str, Any]] = []

    # Scan subdirectories for {model}_{timestamp}.csv files
    for subdir in sorted(results_path.iterdir()):
        if not subdir.is_dir():
            continue

        # Find CSV files matching the pattern {model}_{timestamp}.csv
        for csv_file in sorted(subdir.glob("*.csv")):
            # Skip summary.csv files (those are per-cycle summaries)
            if csv_file.name == "summary.csv":
                continue

            # Read the single-row CSV
            try:
                with open(csv_file, "r", encoding="utf-8", newline="") as f:
                    reader = csv.DictReader(f)
                    for row in reader:
                        # Filter to comparison columns only
                        filtered_row = {k: row.get(k, "") for k in COMPARISON_COLUMNS}
                        model_rows.append(filtered_row)
                        logger.debug(f"Loaded benchmark data from {csv_file.name}")
            except Exception as e:
                logger.warning(f"Failed to read {csv_file}: {e}")

    if not model_rows:
        logger.warning(f"No benchmark CSV files found in {results_dir}")
        # Create empty comparison file
        timestamp = datetime.now().strftime("%Y-%m-%d_%H-%M-%S")
        comparison_path = results_path / f"comparison_{timestamp}.csv"
        with open(comparison_path, "w", encoding="utf-8", newline="") as f:
            writer = csv.DictWriter(f, fieldnames=COMPARISON_COLUMNS)
            writer.writeheader()
        return comparison_path

    # Generate comparison CSV
    timestamp = datetime.now().strftime("%Y-%m-%d_%H-%M-%S")
    comparison_path = results_path / f"comparison_{timestamp}.csv"

    with open(comparison_path, "w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=COMPARISON_COLUMNS)
        writer.writeheader()
        writer.writerows(model_rows)

    logger.info(f"Generated comparison report: {comparison_path}")
    return comparison_path


def print_terminal_summary(results_dir: Path) -> None:
    """Print a formatted comparison summary to the terminal.

    Reads the most recent comparison CSV file and prints a formatted table
    sorted by average delay (ascending, lower is better).

    Args:
        results_dir: Path to the benchmark results directory
    """
    results_path = Path(results_dir)

    if not results_path.exists():
        logger.error(f"Results directory not found: {results_dir}")
        return

    # Find the most recent comparison file
    comparison_files = sorted(
        results_path.glob("comparison_*.csv"),
        key=lambda p: p.stat().st_mtime,
        reverse=True,
    )

    if not comparison_files:
        # Try to generate one
        logger.info("No comparison file found, generating one...")
        comparison_path = generate_comparison_report(results_dir)
    else:
        comparison_path = comparison_files[0]

    # Read comparison data
    rows: list[dict[str, Any]] = []
    with open(comparison_path, "r", encoding="utf-8", newline="") as f:
        reader = csv.DictReader(f)
        for row in reader:
            rows.append(row)

    if not rows:
        print("\n=== Benchmark Comparison Summary ===\n")
        print("No benchmark results found.")
        print(f"Results directory: {results_dir}")
        return

    # Sort by total_delay_avg (ascending, lower is better)
    def get_delay(row: dict[str, Any]) -> float:
        try:
            return float(row.get("total_delay_avg", float("inf")))
        except (ValueError, TypeError):
            return float("inf")

    rows.sort(key=get_delay)

    # Print formatted table
    print("\n" + "=" * 95)
    print("=== Benchmark Comparison Summary ===")
    print("=" * 95)
    print()

    # Table header
    header = (
        f"{'Model':<25} {'Fmt%':>7} {'Constr%':>8} {'Resp(s)':>8} "
        f"{'Queue':>8} {'Delay':>10} {'Thru':>8}"
    )
    print(header)
    print("-" * 95)

    # Table rows
    for row in rows:
        model = row.get("model_name", "unknown")[:24]
        try:
            fmt_rate = float(row.get("format_success_rate", 0)) * 100
        except (ValueError, TypeError):
            fmt_rate = 0.0
        try:
            constr_rate = float(row.get("constraint_satisfaction_rate", 0)) * 100
        except (ValueError, TypeError):
            constr_rate = 0.0
        try:
            resp_time = float(row.get("response_time_avg", 0))
        except (ValueError, TypeError):
            resp_time = 0.0
        try:
            queue = float(row.get("queue_vehicles_avg", 0))
        except (ValueError, TypeError):
            queue = 0.0
        try:
            delay = float(row.get("total_delay_avg", 0))
        except (ValueError, TypeError):
            delay = 0.0
        try:
            throughput = float(row.get("throughput", 0))
        except (ValueError, TypeError):
            throughput = 0.0

        print(
            f"{model:<25} {fmt_rate:>6.1f}% {constr_rate:>7.1f}% "
            f"{resp_time:>8.2f} {queue:>8.1f} {delay:>10.1f} {throughput:>8.2f}"
        )

    print("-" * 95)
    print(f"Total models: {len(rows)}")
    print(f"Results directory: {results_dir}")
    print(f"Comparison file: {comparison_path.name}")
    print("=" * 95 + "\n")

    logger.info(f"Printed terminal summary for {len(rows)} models")
